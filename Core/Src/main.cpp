struct uart_recv_msg{
	bool is_start_bit1 = false;
	const std::array<uint8_t, 2> start_bits;
	std::array<uint8_t, 2> mode;
	std::array<uint8_t, 6> data;

	bool ok_start_bits1(uint8_t id1) {
		if(is_start_bit1)
			return true;

		if(start_bits[0] == id1)
			is_start_bit1 = true;

		return false;
	}

	bool ok_start_bits2 (uint8_t id2) {
		bool tmp = is_start_bit1;
		is_start_bit1 = false;

		if(tmp && start_bits[1] == id2) {
			return true;
		}

		return false;
	}

	void read_mode(uint8_t recv_data[2]) {
		for(size_t i = 0; i < mode.size(); i++) {
			mode[i] = recv_data[i];
		}
	}

	void read_data(uint8_t recv_data[6*sizeof(float)]) {
		for(size_t i = 0; i < data.size(); i++) {
			data[i] = 0;
		}
	}

	uart_recv_msg() = delete;

	uart_recv_msg(uint8_t id1, uint8_t id2) : start_bits({id1, id2}) {
	}

	uart_recv_msg(uint8_t id[2]) : start_bits({id[0], id[1]}) {
	}

	uart_recv_msg(std::array<uint8_t, 2> id): start_bits(id) {
	}

};

