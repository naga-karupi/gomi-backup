  std::array<Vesc_CANBUS_Control_Typedef, 4> us;
  std::array<Vesc_CANBUS_Control_Typedef, 2> shot;

  for(size_t i = 0; i < us.size(); i++) {
	  Vesc_CANBUS_Control_Init(&(us[i]), &hcan2, i);
  }

  Vesc_CANBUS_Control_Init(&(shot[0]), &hcan2, 10);
  Vesc_CANBUS_Control_Init(&(shot[1]), &hcan2, 11);
  HAL_CAN_Start(&hcan2);

  uint8_t header[2] = {'r', 'g'};
  uint8_t mode[2];
  float data[5];

  encs =
  	   {std::make_shared<rotary_encoder_stm32>(&htim1),
  		std::make_shared<rotary_encoder_stm32>(&htim2),
  		std::make_shared<rotary_encoder_stm32>(&htim3),
  		std::make_shared<rotary_encoder_stm32>(&htim5), };

  uint8_t tmp_data[56];
  HAL_UART_Receive(&huart2, tmp_data, 28, 10000ull);
  HAL_UART_Receive(&huart2, tmp_data+28, 28, 10000ull);

